async function safeReadFile(filepath) {
    try {
        return await fs.readFile(filepath, 'utf8');
    } catch (error) {
        switch (error.code) {
            case 'ENOENT':
                console.log('File not found');
                break;
            case 'EACCES':
                console.log('Permission denied');
                break;
            default:
                console.log('Unknown error:', error.message);
        }
        throw error;
    }
}

